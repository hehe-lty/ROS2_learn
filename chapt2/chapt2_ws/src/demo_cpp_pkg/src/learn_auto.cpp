#include "iostream"

int main()
{
    auto a = 10;    // int a = 10;
    auto b = 10.5;  // double b = 10.5;
    auto c = 10.5f; // float c = 10.5f;
    auto d = 'a';   // char d = 'a';

    std::cout << a << std::endl;
    std::cout << b << std::endl;
    std::cout << c << std::endl;
    std::cout << d << std::endl;

    return 0;
}